# Площадь треугольника
base = 4 
height = 5
area = (base * height) / 2
print("Площадь треугольника", area)

# Площадь квадрата
side_a = 4
area = side_a ** 2
print("Площадь квадрата", area)

# Площадь прямоугольника
side_a = 4
side_b = 5
area = side_a * side_b
print("Площадь прямоугольника", area)


# Площадь круга
pi = 3.14159
diameter = 8
radius = diameter / 2
area = pi * radius ** 2
print("Площадь круга", area)